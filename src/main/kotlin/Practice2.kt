fun main() {
    println(42)     // prints the number 42 to the console with a newline at the end
    println('C')    // prints the character C to the console with a newline at the end
    println("Chase")    // prints the string "Chase" to the console with a newline at the end
    println()       // prints a newline

    print(47)       // prints the number 47
    print('A')      // prints the character A
    print("Walter") // prints the string "Walter"
}